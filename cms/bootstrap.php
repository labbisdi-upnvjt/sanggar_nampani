<?php

/*
|--------------------------------------------------------------------------
| Session
|--------------------------------------------------------------------------
*/

session_start();

/*
|--------------------------------------------------------------------------
| Base Path
|--------------------------------------------------------------------------
*/

define(
    'BASE_PATH',
    dirname(__DIR__)
);

/*
|--------------------------------------------------------------------------
| Configuration
|--------------------------------------------------------------------------
*/

$app = require
    BASE_PATH .
    '/config/app.php';

$db = require
    BASE_PATH .
    '/config/db.php';

/*
|--------------------------------------------------------------------------
| CMS Routes
|--------------------------------------------------------------------------
*/

require_once
    __DIR__ .
    '/routes.php';

/*
|--------------------------------------------------------------------------
| Middleware
|--------------------------------------------------------------------------
*/

require_once
    __DIR__ .
    '/middleware.php';

/*
|--------------------------------------------------------------------------
| Router
|--------------------------------------------------------------------------
*/

$page = $_GET['page'] ?? 'dashboard';

$route = $routes[$page] ?? null;

if (!$route) {

    http_response_code(404);

    die('404 Page Not Found');

}

/*
|--------------------------------------------------------------------------
| Authentication
|--------------------------------------------------------------------------
*/

auth($route);

/*
|--------------------------------------------------------------------------
| Load View
|--------------------------------------------------------------------------
*/

require_once
    __DIR__ .
    '/pages/' .
    $route['view'];
?>